package com.evanmclean.evlib.util;

import java.io.Serializable;
import java.util.Comparator;

/**
 * A string comparator that sorts case insensitively first, then by case.
 * 
 * @author Evan M<sup>c</sup>Lean <a href="http://evanmclean.com/"
 *         target="_blank">M<sup>c</sup>Lean Computer Services</a> (see the
 *         overview for copyright and licensing.)
 */
public class CompareCase implements Comparator<String>, Serializable
{
  private static final long serialVersionUID = 1L;

  /** Instance of this comparator. */
  public static final CompareCase INSTANCE = new CompareCase();

  public int compare( final String lhs, final String rhs )
  {
    int ret = lhs.compareToIgnoreCase(rhs);
    if ( ret == 0 )
      ret = lhs.compareTo(rhs);
    return ret;
  }
}
